
    // Q.Explain the key differences between JavaScript and HTML. Provide examples of situations where you would use each
    //    
    //       :-) JavaScript and HTML are both essential components in web development, but they serve different purposes and have distinct roles. Here are the key differences between JavaScript and HTML, along with examples of situations where you would use each:
    //     

    //     
    //         1:- Nature and Purpose
    //    
    //    
    //          JavaScript: It is a scripting language used for adding interactivity, dynamic behavior, and client-side scripting to web pages. JavaScript allows developers to manipulate the DOM, handle events, perform asynchronous operations, and create interactive features.
    //          HTML: It is a markup language used for structuring content on the web. HTML provides the basic building blocks for creating the structure of a webpage, such as headings, paragraphs, lists, images, and links.
    //     
    //    2:- Usage Examples:
    //             JavaScript:
    //             1: Adding interactivity to a webpage: Responding to user actions like clicks, hovers, or form submissions.
    //             2: Validating and processing user input before submitting a form to the server.
    //             3: Fetching and displaying data from external sources or APIs asynchronously.
    //             4: Animating elements on the webpage for a more engaging user experience.
    //    
    //     
    //         HTML:
    //             1: Defining the structure of a webpage using tags like <html>, <head>, <body>, <header>, <footer>, etc.
    //             2: Creating text content, headings, paragraphs, lists, images, and links.
    //             3: Embedding multimedia content using <img>, <audio>, and <video> tags.
    //             4: Building forms to collect user input.
    //       3:- Syntax
    //     
      //      JavaScript: It has a more complex syntax compared to HTML. JavaScript code is typically embedded in HTML documents using script tags, either inline or as external files.
    //         <script>
    //             // JavaScript code goes here
    //             function greet() {
    //               alert('Hello, World!');
    //             }
    //           </script>
          
    //         HTML: It has a simpler and more declarative syntax based on tags. Tags are used to define elements and their structure.
    //                <!DOCTYPE html>
    //                 <html>
    //                 <head>
    //                     <title>My Webpage</title>
    //                 </head>
    //                 <body>
    //                     <h1>Welcome to My Webpage</h1>
    //                     <p>This is a sample paragraph.</p>
    //                 </body>
    //                 </html>

                           
            
    //                 </body>
    //                 </html>

    //    4:- Execution Context:

    //            
    //                 JavaScript: It is executed by the browser on the client side. JavaScript code runs in the context of the user's browser, allowing for dynamic updates and interactions without requiring server communication.
    //                 HTML: It is parsed and rendered by the browser to create the structure and layout of the webpage. HTML is static and defines the initial state of the page.
    //          
                
               
    //     5:- File Types:   
    //            
    //                 JavaScript: It is typically written in separate files with a ".js" extension and linked to HTML documents. For example, "script.js."
    //                 HTML: It is usually written in ".html" files and contains the structure and content of the webpage. For example, "index.html."
